using System;

class Program
{
    static void Main(string[] args)
    {
        int i = 1; // Sayacımızı başlatıyoruz
        while (i <= 10) // Koşul: i, 10'dan küçük veya eşit olduğu sürece
        {
            Console.WriteLine("Kendime inanıyorum, ben bu yazılım işini hallederim!");
            i++; // Sayacı artırıyoruz
        }
    }
}
